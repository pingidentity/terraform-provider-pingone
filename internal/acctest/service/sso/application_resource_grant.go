func ApplicationResourceGrant_RemovalDrift_PreConfig(ctx context.Context, apiClient *management.APIClient, t *testing.T, environmentID, applicationID, applicationResourceGrantID string) {
	if environmentID == "" || applicationID == "" || applicationResourceGrantID == "" {
		t.Fatalf("One of environment ID, application ID or application resource grant ID cannot be determined. Environment ID: %s, Application ID: %s, Application Resource Grant ID: %s", environmentID, applicationID, applicationResourceGrantID)
	}

	_, err := apiClient.ApplicationResourceGrantsApi.DeleteApplicationGrant(ctx, environmentID, applicationID, applicationResourceGrantID).Execute()
	if err != nil {
		t.Fatalf("Failed to delete application resource grant: %v", err)
	}
}
